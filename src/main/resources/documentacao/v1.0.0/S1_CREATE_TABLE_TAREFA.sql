
    CREATE TABLE TAREFA (
        DATA_VENCIMENTO DATE,
        DATA_ATUALIZACAO TIMESTAMP(6),
        DATA_CRIACAO TIMESTAMP(6),
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
        USUARIO_ID UUID,
        DESCRICAO VARCHAR(500),
        TITULO VARCHAR(255) NOT NULL,
        STATUS VARCHAR(20) NOT NULL,
        PRIMARY KEY (ID)
    )